package com.company.multithreading.threadinterface;

public class ThreadInterfaceExample {
    void loop1() {
        for (int i = 0; i < 5; i++) {
            System.out.println("First Loop " + i);
        }

    }

    void loop2() {
        for (int j = 0; j < 5; j++) {
            System.out.println("Second Loop " + j);
        }

    }

}
